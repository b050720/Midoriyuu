const iconCache = new Map();

function createSVG(viewBox, path) {
    const svgNS = "http://www.w3.org/2000/svg";
    const svg = document.createElementNS(svgNS, "svg");
    svg.setAttribute("viewBox", viewBox);
    svg.setAttribute("width", "100%");
    svg.setAttribute("height", "100%");
    svg.setAttribute("aria-hidden", "true");

    const p = document.createElementNS(svgNS, "path");
    p.setAttribute("d", path);
    svg.appendChild(p);
    return svg;
}

class MyIcon extends HTMLElement {
    connectedCallback() {
        const name = this.getAttribute("name") || "bookmark";
        if (!iconList[name]) return;

        // 如果快取沒有，就當場建立並存入快取
        if (!iconCache.has(name)) {
            const { viewBox, path } = iconList[name];
            iconCache.set(name, createSVG(viewBox, path));
        }

        // 統一從快取拿出來 clone 並塞入元件
        this.replaceChildren(iconCache.get(name).cloneNode(true));
    }
}

